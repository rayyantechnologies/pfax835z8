                    <p class="text-muted mb-1">Pride of faith Academy school secondary school established in Sagamu and commenced operations officially on 16th September 2016. Pride of faith Academy is duly registered with the ministry of education science and technology Ogun state. The school is owned by Pride of Faith Foundation, a foundation whose focus is human capital development in the areas of Islamic education, health and community services.</p>
